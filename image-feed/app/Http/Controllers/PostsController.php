    {
        //
        $path = $request->file('image')->store('private');
        $image = Image::create([
            'name' =>  basename($path),
        ]);
        Post::create([
            'title' => $request->input('title'),
            'image_id' => $image->id,
            'user_id' => Auth::id()
        ]);
    }

